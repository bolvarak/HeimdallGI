#include "QtCore/QLatin1String"
#include "QtCore/QObject"
#include "QtCore/QString"
#include "QtCore/QStringList"
#include "QtSql/QSqlDatabase"
#include "QtSql/QSqlError"

///////////////////////////////////////////////////////////////////////////////
/// HeimdallGI Namespace /////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////

namespace HeimdallGI
{
	///////////////////////////////////////////////////////////////////////////
	/// HeimdallGI::Pool Namespace ///////////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////

	namespace Pool
	{
		///////////////////////////////////////////////////////////////////////
		/// HeimdallGI::Pool::Database Class Definition //////////////////////
		/////////////////////////////////////////////////////////////////////

		class Database : public QObject
		{
			///////////////////////////////////////////////////////////////////
			/// QObject Classification ///////////////////////////////////////
			/////////////////////////////////////////////////////////////////

			Q_OBJECT;

			///////////////////////////////////////////////////////////////////
			/// Protected Methods & Properties ///////////////////////////////
			/////////////////////////////////////////////////////////////////

			protected:

				///////////////////////////////////////////////////////////////
				/// Properties ///////////////////////////////////////////////
				/////////////////////////////////////////////////////////////

				/**
				 * This property contains the names in the connection pool
				 * @access protected
				 * @name HeimdallGI::Pool::Database::mConnections
				 * @static
				 * @var QStringList
				 */
				static QStringList mConnections;

			///////////////////////////////////////////////////////////////////
			/// Public Methods & Properties //////////////////////////////////
			/////////////////////////////////////////////////////////////////

			public:

				///////////////////////////////////////////////////////////////
				/// Static Methods ///////////////////////////////////////////
				/////////////////////////////////////////////////////////////

				/**
				 * This method adds a database to the connection pool
				 * @access public
				 * @name HeimdallGI::Pool::Database::add()
				 * @param QString strDriver
				 * @param QString strHost
				 * @param int intPort
				 * @param QString strDatabase
				 * @param QString strUsername
				 * @param QString strPassword
				 * @param QString strConnectionName [""]
				 * @return bool
				 * @static
				 */
				static bool add(QString strDriver, QString strHost, int intPort, QString strDatabase, QString strUsername, QString strPassword, QString strConnectionName = "");

				/**
				 * This method retrieves a database connection from the pool
				 * @access public
				 * @name HeimdallGI::Pool::Database::get()
				 * @param QString strConnectionName [""]
				 * @return QSqlDatabase
				 * @static
				 */
				static QSqlDatabase get(QString strConnectionName = "");

				/**
				 * This method removes a database connection from the pool
				 * @access public
				 * @name HeimdallGI::Pool::Database::remove()
				 * @param QString strConnectionName [""]
				 * @return void
				 * @static
				 */
				static void remove(QString strConnectionName = "");
		};

	///////////////////////////////////////////////////////////////////////////
	/// End HeimdallGI::Pool Namespace ///////////////////////////////////////
	/////////////////////////////////////////////////////////////////////////

	}

///////////////////////////////////////////////////////////////////////////////
/// End HeimdallGI Namespace /////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////

}

